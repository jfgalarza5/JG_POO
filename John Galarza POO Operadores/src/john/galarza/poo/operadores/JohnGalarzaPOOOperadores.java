public class JohnGalarzaPOOOperadores {
    
    public static void main(String[] args) {
        //Numeros
        int a=1;
        int b=7;
        
        //Suma
        int suma = a+b;
        int resta=a-b;
        int multiplicacion=a*b;
        int division=a/b;

        System.out.println("Primer numero: ");
        System.out.println(a);
        System.out.println("Primer numero +1: ");
        System.out.println(++a);
        System.out.println("Segundo numero: ");
        System.out.println(b);
        System.out.println("Segundo numero +1: ");
        System.out.println(++b);
        System.out.println("Suma de los dos numeros: ");
        System.out.println(suma);
        System.out.println("Resta de dos numeros: ");
        System.out.println(resta);
        System.out.println("Multiplicacion de dos numeros: ");
        System.out.println(multiplicacion);
        System.out.println("Division de dos numeros: ");
        System.out.println(division);
    }
    
}
